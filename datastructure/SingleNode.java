package datastructure;

public class SingleNode {

    int value;
    SingleNode left;

    public void setLeft(SingleNode left) {
        this.left = left;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public SingleNode getLeft() {
        return left;
    }
}
